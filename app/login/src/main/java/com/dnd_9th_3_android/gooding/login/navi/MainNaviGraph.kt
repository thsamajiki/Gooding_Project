package com.dnd_9th_3_android.gooding.login.navi

import androidx.compose.animation.ExperimentalAnimationApi
import androidx.compose.runtime.Composable

import androidx.navigation.NavHostController
import androidx.navigation.compose.NavHost
import androidx.navigation.compose.composable
import com.dnd_9th_3_android.gooding.login.OnBoardingScreen
import com.dnd_9th_3_android.gooding.login.SplashScreen
import com.dnd_9th_3_android.gooding.login.ui.SosLoginScreen


@OptIn(ExperimentalAnimationApi::class)
@Composable
fun MainNaviGraph(
    navController: NavHostController,
){
    NavHost(
        navController = navController,
        startDestination = "splashScreen"
    ){
        composable("splashScreen"){ SplashScreen(navController) }
        composable("sosScreen"){ SosLoginScreen(navController) }
        composable("onBoardingScreen"){OnBoardingScreen()}
    }
}